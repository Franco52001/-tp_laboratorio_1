#include <stdio.h>
/** \brief Pide numero
 *
 * \param char[]Mensaje transmitido a la hora de pedir el numero
 * \return float Tipo de numero que pide
 *
 */
float pedirNum(char[]);
/** \brief Es la estructura de la calculadora
 *
 * \param numUno float Primer numero utilizado
 * \param numDos float Segundo numero utilizado
 * \return void
 *
 */
void  calcular(float numUno,float numDos);
/** \brief Realiza la operacion suma
 *
 * \param numUno float Primer numero utilizado en la suma
 * \param numDos float Segundo numero utilizado en la suma
 * \return float
 *
 */
float sumar(float numUno, float numDos);
/** \brief Realiza la operacion resta
 *
 * \param numUno float Primer numero utilizado en la resta
 * \param numDos float Segundo numero utilizado en la resta
 * \return float
 *
 */
float restar(float numUno, float numDos);
/** \brief Realiza la operacion multiplicacion
 *
 * \param numUno float primer numero utilizado en la multiplicacion
 * \param numDos float Segundo numero utilizado en la multiplicacion
 * \return float
 *
 */
float multiplicar(float numUno, float numDos);
/** \brief Realiza la operacion division
 *
 * \param numUno float Primer numero utilizado en la division
 * \param numDos float Segundo numero utilizado en la division
 * \return float
 *
 */
float dividir(float numUno, float numDos);
/** \brief Realiza el factorial del primer numero
 *
 * \param numUno float Primer numero al cual se le aplicara la funcion factorial
 * \return float
 *
 */
float factorialUno(float numUno);
/** \brief Realiza la operacion del segundo numero
 *
 * \param numDos float Segundo numero al cual se le aplicara la funcion factorial
 * \return float
 *
 */
float factorialDos(float numDos);
